CREATE TABLE publishers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    name VARCHAR(160) NOT NULL UNIQUE,
    canonical_domain VARCHAR(255),
    official BOOLEAN NOT NULL DEFAULT FALSE,
    trust_score NUMERIC(5, 4) NOT NULL DEFAULT 0.5000,
    active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT chk_publishers_trust_score CHECK (trust_score >= 0 AND trust_score <= 1)
);

ALTER TABLE news_sources
    ADD COLUMN publisher_id BIGINT REFERENCES publishers(id),
    ADD COLUMN provider VARCHAR(32) NOT NULL DEFAULT 'rss',
    ADD COLUMN config_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    ADD COLUMN config_version INTEGER NOT NULL DEFAULT 1,
    ADD COLUMN credential_ref VARCHAR(120),
    ADD COLUMN fetch_interval_seconds INTEGER NOT NULL DEFAULT 900;

INSERT INTO publishers (
    created_at,
    updated_at,
    name,
    canonical_domain,
    official,
    trust_score,
    active
)
SELECT
    CURRENT_TIMESTAMP,
    CURRENT_TIMESTAMP,
    source.name,
    NULL,
    FALSE,
    0.5000,
    source.active
FROM news_sources source
ON CONFLICT (name) DO NOTHING;

UPDATE news_sources source
SET publisher_id = publisher.id
FROM publishers publisher
WHERE publisher.name = source.name
  AND source.publisher_id IS NULL;

