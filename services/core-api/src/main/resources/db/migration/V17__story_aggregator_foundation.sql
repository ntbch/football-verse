    ADD COLUMN last_source_at TIMESTAMP(6) WITH TIME ZONE,
    ADD COLUMN last_material_change_at TIMESTAMP(6) WITH TIME ZONE,
    ADD COLUMN source_count_cached INTEGER NOT NULL DEFAULT 0,
