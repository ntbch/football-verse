create table tracked_matches (
    id bigint generated by default as identity primary key,
    fixture_id varchar(60) not null,
    league_slug varchar(60) not null,
    round varchar(120),
    home_team varchar(120) not null,
    away_team varchar(120) not null,
    kickoff timestamp(6) with time zone not null,
    home_score integer,
    away_score integer,
    status varchar(20) not null default 'upcoming',
    scored boolean not null default false,
    unique (fixture_id)
);

create table user_predictions (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    user_id bigint not null references users(id),
    match_id bigint not null references tracked_matches(id),
    pick varchar(10) not null,
    home_score integer,
    away_score integer,
    points integer not null default 0,
    correct boolean not null default false,
    unique (user_id, match_id)
);

create index idx_user_predictions_user on user_predictions(user_id);
create index idx_user_predictions_match on user_predictions(match_id);

create table user_prediction_stats (
    id bigint generated by default as identity primary key,
    user_id bigint not null unique references users(id),
    total_points integer not null default 0,
    correct_picks integer not null default 0,
    total_picks integer not null default 0,
    current_streak integer not null default 0,
    best_streak integer not null default 0
);

create table user_badges (
    id bigint generated by default as identity primary key,
    user_id bigint not null references users(id),
    badge_code varchar(40) not null,
    awarded_at timestamp(6) with time zone not null,
    unique (user_id, badge_code)
);

create index idx_user_badges_user on user_badges(user_id);
