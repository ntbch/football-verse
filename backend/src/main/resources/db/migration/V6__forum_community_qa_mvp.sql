ALTER TABLE forum_threads
    ADD COLUMN solved BOOLEAN NOT NULL DEFAULT FALSE,
    ADD COLUMN best_answer_post_id BIGINT REFERENCES forum_posts(id) ON DELETE SET NULL,
    ADD COLUMN last_activity_at TIMESTAMP;

UPDATE forum_threads t
SET last_activity_at = COALESCE(
    (SELECT MAX(p.created_at) FROM forum_posts p WHERE p.thread_id = t.id),
    t.created_at
)
WHERE last_activity_at IS NULL;

ALTER TABLE forum_threads
    ALTER COLUMN last_activity_at SET NOT NULL;

CREATE INDEX idx_forum_threads_cat_hid_pin_activity
    ON forum_threads(category_id, hidden, pinned DESC, last_activity_at DESC);

CREATE TABLE forum_thread_follows (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    thread_id BIGINT NOT NULL REFERENCES forum_threads(id) ON DELETE CASCADE,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (thread_id, user_id)
);

