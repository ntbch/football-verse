create table users (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    email varchar(120) not null unique,
    username varchar(60) not null unique,
    password_hash varchar(255) not null,
    status varchar(20) not null
);

create table user_roles (
    user_id bigint not null references users(id),
    role varchar(30) not null,
    primary key (user_id, role)
);

create table user_profiles (
    id bigint generated by default as identity primary key,
    user_id bigint not null unique references users(id),
    display_name varchar(80) not null,
    avatar_url varchar(255),
    bio varchar(500)
);

create table refresh_tokens (
    id bigint generated by default as identity primary key,
    user_id bigint not null references users(id),
    token varchar(80) not null unique,
    expires_at timestamp(6) with time zone not null,
    revoked_at timestamp(6) with time zone
);

create table news_categories (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    name varchar(100) not null unique,
    slug varchar(120) not null unique
);

create table news_tags (
    id bigint generated by default as identity primary key,
    name varchar(80) not null unique,
    slug varchar(100) not null unique
);

create table news_sources (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    name varchar(120) not null unique,
    feed_url varchar(255) not null unique,
    active boolean not null,
    last_crawled_at timestamp(6) with time zone
);

create table news_articles (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    title varchar(200) not null,
    slug varchar(240) not null unique,
    summary varchar(500),
    content text not null,
    status varchar(20) not null,
    category_id bigint references news_categories(id),
    source_id bigint references news_sources(id),
    author_id bigint references users(id),
    source_url varchar(255) unique,
    content_hash varchar(64) unique,
    published_at timestamp(6) with time zone
);

create index idx_news_articles_status_pub on news_articles(status, published_at desc);

create table news_article_tags (
    article_id bigint not null references news_articles(id),
    tag_id bigint not null references news_tags(id),
    primary key (article_id, tag_id)
);

create table news_likes (
    id bigint generated by default as identity primary key,
    article_id bigint not null references news_articles(id),
    user_id bigint not null references users(id),
    unique (article_id, user_id)
);

create table news_bookmarks (
    id bigint generated by default as identity primary key,
    article_id bigint not null references news_articles(id),
    user_id bigint not null references users(id),
    unique (article_id, user_id)
);

create table news_comments (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    article_id bigint not null references news_articles(id),
    author_id bigint not null references users(id),
    parent_id bigint references news_comments(id),
    content varchar(2000) not null
);

create index idx_news_comments_art_created on news_comments(article_id, created_at asc);

create table forum_categories (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    name varchar(100) not null unique,
    slug varchar(120) not null unique
);

create table forum_threads (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    title varchar(180) not null,
    slug varchar(220) not null unique,
    category_id bigint not null references forum_categories(id),
    author_id bigint not null references users(id),
    pinned boolean not null,
    locked boolean not null,
    hidden boolean not null
);

create index idx_forum_threads_cat_hid_pin_created on forum_threads(category_id, hidden, pinned desc, created_at desc);

create table forum_posts (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    thread_id bigint not null references forum_threads(id),
    author_id bigint not null references users(id),
    content text not null,
    hidden boolean not null
);

create index idx_forum_posts_thread_hid_created on forum_posts(thread_id, hidden, created_at asc);

create table forum_reports (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    reporter_id bigint not null references users(id),
    target_type varchar(20) not null,
    target_id bigint not null,
    reason varchar(500) not null,
    status varchar(20) not null
);

create table notifications (
    id bigint generated by default as identity primary key,
    created_at timestamp(6) with time zone not null,
    updated_at timestamp(6) with time zone not null,
    user_id bigint not null references users(id),
    type varchar(40) not null,
    message varchar(300) not null,
    link_url varchar(255),
    read_at timestamp(6) with time zone
);

create index idx_notifications_user_created on notifications(user_id, created_at desc);
